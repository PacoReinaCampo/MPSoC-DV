function RHO_OUT = dnc_interface_matrix(U_IN, H_IN)
  % Package
  addpath(genpath('../../math/algebra/tensor'));

  % Constants
  [SIZE_R_IN, ~, SIZE_L_IN] = size(U_IN);

  % Internal Signals
  matrix_h_int = zeros(SIZE_R_IN, SIZE_L_IN);

  % Body
  % rho(t;i;m) = U(i;m;l)·h(t;i;l)
  for i = 1:SIZE_R_IN
    for l = 1:SIZE_L_IN
      matrix_h_int(i, l) = H_IN(l);
    end
  end

  RHO_OUT = ntm_tensor_matrix_product(U_IN, matrix_h_int);
end